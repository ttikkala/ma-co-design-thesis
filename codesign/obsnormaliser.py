import torch

""" 
    This script contains the observation normaliser class which handles normalising new observations
    based on an exponentially moving average updated throughout training.
"""

class ObservationNormaliser:
    def __init__(self, shape,  epsilon=1e-4, device='cuda'):
        self.shape = shape
        self.device = device
        self.epsilon = epsilon
        self.means = torch.ones(self.shape, device=self.device) * self.epsilon
        self.vars = torch.ones(self.shape, device=self.device)
        self.momentum = 0.99
    
    def reset(self):
        self.means = torch.ones(self.shape, device=self.device) * self.epsilon
        self.vars = torch.ones(self.shape, device=self.device)

    def update_batch(self, batch):
        # update with momentum https://kaixih.github.io/batch-norm/
        batch_mean = torch.mean(batch, dim=0)
        batch_var = torch.var(batch, dim=0, unbiased=False)
        
        self.means = self.momentum * self.means + (1 - self.momentum) * batch_mean
        self.vars = self.momentum * self.vars + (1 - self.momentum) * batch_var
        
        self.vars = torch.clamp(self.vars, min=self.epsilon)

    def get_means_vars(self):
                
        return (self.means, self.vars)
    
    def set_means_vars(self, new_means, new_vars):

        self.means = new_means
        self.vars = new_vars

    
    def normalise(self, obs):
        # Leave out teammate spread out, home score, away score and design parameters from normalisation
        
        normalised_obs = obs.clone()
        
        # Normalise first player ignoring teammate spread out, home score, away score and design parameters
        normalised_obs[..., :301] = (obs[..., :301] - self.means[:301]) / torch.sqrt(self.vars[:301] + self.epsilon)
        
        # Normalise second player
        normalised_obs[..., 308:609] = (obs[..., 308:609] - self.means[308:609]) / torch.sqrt(self.vars[308:609] + self.epsilon)
        
        # Normalise third player
        normalised_obs[..., 616:917] = (obs[..., 616:917] - self.means[616:917]) / torch.sqrt(self.vars[616:917] + self.epsilon)
        
        # Normalise fourth player
        normalised_obs[..., 924:1225] = (obs[..., 924:1225] - self.means[924:1225]) / torch.sqrt(self.vars[924:1225] + self.epsilon)
        
        return normalised_obs


