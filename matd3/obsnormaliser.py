import numpy as np
import torch

""" 
    This script contains the observation normaliser class which handles normalising new observations
    based on an exponentially moving average updated throughout training.
"""

class ObservationNormaliser:
    def __init__(self, shape, epsilon=1e-4, device='cuda'):
        self.shape = shape
        self.device = device
        self.means = torch.zeros(self.shape, device=self.device)
        self.vars = torch.ones(self.shape, device=self.device)
        self.count = epsilon
        self.epsilon = epsilon
        self.sum = torch.zeros(self.shape, device=self.device)
        self.sum_sq = torch.zeros(self.shape, device=self.device)
        self.momentum = 0.99
    
    def reset(self):
        self.means = torch.zeros(self.shape, device=self.device)
        self.vars = torch.ones(self.shape, device=self.device)
        self.count = self.epsilon
        self.sum = torch.zeros(self.shape, device=self.device)
        self.sum_sq = torch.zeros(self.shape, device=self.device)
    

    def update_batch(self, batch):
        # update with momentum https://kaixih.github.io/batch-norm/
        batch_mean = torch.mean(batch, dim=0)
        batch_var = torch.var(batch, dim=0, unbiased=False)
        
        self.means = self.momentum * self.means + (1 - self.momentum) * batch_mean
        self.vars = self.momentum * self.vars + (1 - self.momentum) * batch_var
        
        self.vars = torch.clamp(self.vars, min=self.epsilon)


    def get_means_vars(self):
        
        (means, variances) = (self.means, self.vars)
        
        return (means, variances)
    
    def normalise(self, obs):
        return (obs - self.means) / torch.sqrt(self.vars + self.epsilon)

